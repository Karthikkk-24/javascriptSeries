// Comparison Operators

const x = 10;

const y = 20;

console.log(x > y); // false

console.log(x < y); // true

console.log(x >= y); // false

console.log(x <= y); // true

// loose comparison
console.log(x == y); // false

// strict comparison
console.log(x === y); // false

// loose comparison
console.log(x != y); // true

// strict comparison
console.log(x !== y); // true