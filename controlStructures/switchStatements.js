// Switch Statement 

const month = 'february';

switch (month) {
    case "january":
        console.log('january');
        break;
    case "february":
        console.log('february'); // february
        break;
    case "march":
        console.log('march');
        break;
    case "april":
        console.log('april');
        break;
    case "may":
        console.log('may');
        break;
    case "june":
        console.log('june');
        break;
    case "july":
        console.log('july');
        break;
    case "august":
        console.log('august');
        break;
    case "september":
        console.log('september');
        break;
    case "october":
        console.log('october');
        break;
    case "november":
        console.log('november');
        break;
    case "december":
        console.log('december');
        break;
    default:
        console.log('Incorrect Month');
        break;
}