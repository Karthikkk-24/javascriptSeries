// Unary Operators

let num = 10;

let negativeNum = -num;

console.log(negativeNum); // -10

let positiveNum = +negativeNum;

console.log(positiveNum); // -10

let doubleNegative = -(-num);

console.log(doubleNegative); // 10

// Post Increment Operators
num++;
console.log(num); // 11

// Post Decrement Operators
num--;
console.log(num); // 10

// Pre Increment Operators
++num;
console.log(num); // 11

// Pre Decrement Operators
--num;
console.log(num); // 10